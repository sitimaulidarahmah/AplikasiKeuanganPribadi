    //Menambah transaksi baru.
    // Mengambil data dari form, lalu menyimpannya ke database.
    //Setelah itu tabel dan dashboard diperbarui.
    private void tambahTransaksi() {
        try {
            Date tanggal = jDateChooserTanggal.getDate();
            if (tanggal == null) {
                JOptionPane.showMessageDialog(this, "Pilih tanggal terlebih dahulu!");
                return;
            }

            String tipe = rbPemasukan.isSelected() ? "Pemasukan" : "Pengeluaran";
            String kategori = cmbKategori.getSelectedItem().toString();
            double jumlah = Double.parseDouble(txtJumlah.getText());
            String deskripsi = txtAreaDeskripsi.getText();

            Transaksi t = new Transaksi(tanggal, tipe, kategori, jumlah, deskripsi);

            transaksiDAO.insert(t);      // <-- Simpan ke database
            loadDataFromDatabase();      // <-- Refresh tabel

            resetForm();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Input tidak valid!");
        }
    }

