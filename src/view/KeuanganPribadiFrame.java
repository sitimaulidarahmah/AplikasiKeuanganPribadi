    private void exportData() {
        // 1. User pilih lokasi & nama file
        JFileChooser chooser = new JFileChooser();
        chooser.setDialogTitle("Pilih lokasi untuk menyimpan data keuangan");
        chooser.setSelectedFile(new File("data_keuangan.txt")); // nama default

        int result = chooser.showSaveDialog(this);
        if (result != JFileChooser.APPROVE_OPTION) {
            return; // user batal
        }

        // File yang dipilih user
        File userFile = chooser.getSelectedFile();
        // File internal yang akan dipakai import otomatis
        File internalFile = new File("data_keuangan.txt");

        try (FileWriter writerUser = new FileWriter(userFile, false); FileWriter writerInternal = new FileWriter(internalFile, false)) {

            // Header CSV
            String header = "Tanggal,Tipe,Kategori,Jumlah,Deskripsi\n";
            writerUser.write(header);
            writerInternal.write(header);

            SimpleDateFormat sdf = new SimpleDateFormat("dd MMM yyyy");

            for (Transaksi t : manager.getDaftarTransaksi()) {

                String tanggal = sdf.format(t.getTanggal());
                String tipe = t.getTipe();
                String kategori = t.getKategori();

                long jumlahLong = Math.round(t.getJumlah());
                String jumlah = String.valueOf(jumlahLong);

                String deskripsi = t.getDeskripsi().replace(",", ";");

                String line = tanggal + ","
                        + tipe + ","
                        + kategori + ","
                        + jumlah + ","
                        + deskripsi + "\n";

                writerUser.write(line);
                writerInternal.write(line);
            }

            JOptionPane.showMessageDialog(this,
                    "Export berhasil!\n"
                    + "File pengguna: " + userFile.getAbsolutePath()
                    + "\nFile untuk import otomatis: " + internalFile.getName());

        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal export: " + e.getMessage());
        }
    }

    private void importData() {
        File file = new File("data_keuangan.txt");
        if (!file.exists()) {
            JOptionPane.showMessageDialog(this,
                    "File data_keuangan.txt tidak ditemukan!");
            return;
        }
        try (BufferedReader reader = new BufferedReader(new FileReader(file))) {

            List<Transaksi> list = new ArrayList<>();
            SimpleDateFormat sdf = new SimpleDateFormat("dd MMM yyyy");

            String line = reader.readLine(); // baca baris pertama

            // cek apakah ini header CSV baru (Tanggal,Tipe,...)
            boolean punyaHeader = false;
            if (line != null && line.toLowerCase().startsWith("tanggal")) {
                punyaHeader = true;
            } else {
                // kalau bukan header, proses baris pertama juga
                // dengan menaruh kembali ke variabel line
            }

            if (!punyaHeader) {
                // baris pertama bukan header, proses nanti di while
            }

            if (!punyaHeader && line != null) {
                // proses baris pertama format lama (timestamp CSV)
                String[] p = line.split(",");
                if (p.length >= 5) {
                    String tanggalText = p[0].trim();
                    Date tanggal;

                    if (tanggalText.matches("\\d+")) {
                        long millis = Long.parseLong(tanggalText);
                        tanggal = new Date(millis);
                    } else {
                        tanggal = sdf.parse(tanggalText);
                    }

                    String tipe = p[1].trim();
                    String kategori = p[2].trim();
                    double jumlah = Double.parseDouble(p[3].trim());
                    String deskripsi = p[4].trim();

                    list.add(new Transaksi(tanggal, tipe, kategori, jumlah, deskripsi));
                }
            }

            while ((line = reader.readLine()) != null) {
                if (line.trim().isEmpty()) {
                    continue;
                }
                String[] p = line.split(",");
                if (p.length < 5) {
                    continue;
                }
                String tanggalText = p[0].trim();
                Date tanggal;

                if (tanggalText.matches("\\d+")) {
                    long millis = Long.parseLong(tanggalText);
                    tanggal = new Date(millis);
                } else {
                    tanggal = sdf.parse(tanggalText);
                }

                String tipe = p[1].trim();
                String kategori = p[2].trim();
                double jumlah = Double.parseDouble(p[3].trim());
                String deskripsi = p[4].trim();

                list.add(new Transaksi(tanggal, tipe, kategori, jumlah, deskripsi));
            }

            transaksiDAO.deleteAll();
            for (Transaksi t : list) {
                transaksiDAO.insert(t);
            }

            loadDataFromDatabase();
            JOptionPane.showMessageDialog(this, "Import berhasil!");

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Gagal import: " + e.getMessage());
        }
    }

    //Menghapus satu transaksi berdasarkan baris yang dipilih di tabel.
    private void hapusTransaksi() {
        int row = tblTransaksi.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih transaksi yang ingin dihapus!");
            return;
        }
        int id = manager.getDaftarTransaksi().get(row).getId();
        try {
            transaksiDAO.delete(id);
            loadDataFromDatabase();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus transaksi!");
        }
    }

    //Mengisi form input berdasarkan baris tabel yang dipilih (mode edit)
    private void editTransaksi() {
        int row = tblTransaksi.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih transaksi yang ingin diedit!");
            return;
        }
        editIndex = row;
        Transaksi t = manager.getDaftarTransaksi().get(row);
        jDateChooserTanggal.setDate(t.getTanggal());
        if (t.getTipe().equals("Pemasukan")) {
            rbPemasukan.setSelected(true);
            setKategoriPemasukan();
        } else {
            rbPengeluaran.setSelected(true);
            setKategoriPengeluaran();
        }

        cmbKategori.setSelectedItem(t.getKategori());
        txtJumlah.setText(String.valueOf((int) t.getJumlah()));
        txtAreaDeskripsi.setText(t.getDeskripsi());

        // memastikan UI update
        jDateChooserTanggal.repaint();
        cmbKategori.repaint();
        txtJumlah.repaint();
        txtAreaDeskripsi.repaint();
    }

    //Mengisi ulang tabel transaksi dengan data terbaru.
    //Dipanggil setiap kali data bertambah, dihapus, atau diedit.
    private void refreshTabel() {
        DefaultTableModel model = (DefaultTableModel) tblTransaksi.getModel();
        model.setRowCount(0);

        
        SimpleDateFormat sdf = new SimpleDateFormat("dd MMM yyyy");

        
        java.text.NumberFormat rupiahFormat = java.text.NumberFormat.getCurrencyInstance(new java.util.Locale("id", "ID"));

        for (Transaksi t : manager.getDaftarTransaksi()) {
            String tanggalFormat = sdf.format(t.getTanggal());
            String jumlahFormat = rupiahFormat.format(t.getJumlah());

            model.addRow(new Object[]{
                tanggalFormat,
                t.getTipe(),
                t.getKategori(),
                jumlahFormat,
                t.getDeskripsi()
            });
        }
    }

    //Mengatur tampilan awal tabel.
    //Menentukan kolom, ukuran kolom, dan tampilan teks pada tabel.
    private void initTabel() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Tanggal");
        model.addColumn("Tipe Transaksi");
        model.addColumn("Kategori");
        model.addColumn("Jumlah");
        model.addColumn("Deskripsi");

        tblTransaksi.setModel(model);

        tblTransaksi.setRowSelectionAllowed(true);
        tblTransaksi.setColumnSelectionAllowed(false);
        tblTransaksi.setSelectionMode(javax.swing.ListSelectionModel.SINGLE_SELECTION);

        // ðŸ”¹ Matikan auto-resize agar lebar kolom bisa diatur manual
        tblTransaksi.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_OFF);

        // ðŸ”¹ Atur lebar tiap kolom agar proporsional
        tblTransaksi.getColumnModel().getColumn(0).setPreferredWidth(120); // Tanggal
        tblTransaksi.getColumnModel().getColumn(1).setPreferredWidth(120); // Tipe Transaksi
        tblTransaksi.getColumnModel().getColumn(2).setPreferredWidth(100); // Kategori
        tblTransaksi.getColumnModel().getColumn(3).setPreferredWidth(100); // Jumlah
        tblTransaksi.getColumnModel().getColumn(4).setPreferredWidth(250); // Deskripsi

        // ðŸ”¹ Tambahkan renderer supaya teks di kolom Deskripsi bisa lebih dari satu baris
        tblTransaksi.getColumnModel().getColumn(4).setCellRenderer(new javax.swing.table.DefaultTableCellRenderer() {
            @Override
            public java.awt.Component getTableCellRendererComponent(javax.swing.JTable table, Object value,
                    boolean isSelected, boolean hasFocus, int row, int column) {

                javax.swing.JTextArea textArea = new javax.swing.JTextArea(value == null ? "" : value.toString());
                textArea.setLineWrap(true);
                textArea.setWrapStyleWord(true);
                textArea.setOpaque(true);

                if (isSelected) {
                    textArea.setBackground(table.getSelectionBackground());
                    textArea.setForeground(table.getSelectionForeground());
                } else {
                    textArea.setBackground(table.getBackground());
                    textArea.setForeground(table.getForeground());
                }

                table.setRowHeight(row, Math.max(30, textArea.getPreferredSize().height));
                return textArea;
            }
        });
    }

    //Mencari transaksi berdasarkan kata yang diketik di kolom pencarian.
    //Jika ada yang cocok, hanya data itu yang ditampilkan.
    private void filterTabel() {
        String keyword = txtCari.getText().toLowerCase();
        DefaultTableModel model = (DefaultTableModel) tblTransaksi.getModel();
        model.setRowCount(0);

        SimpleDateFormat sdf = new SimpleDateFormat("dd MMM yyyy");
        java.text.NumberFormat rupiahFormat = java.text.NumberFormat.getCurrencyInstance(new java.util.Locale("id", "ID"));

        for (Transaksi t : manager.getDaftarTransaksi()) {

            String tanggal = sdf.format(t.getTanggal()).toLowerCase();
            String tipe = t.getTipe().toLowerCase();
            String kategori = t.getKategori().toLowerCase();
            String jumlah = rupiahFormat.format(t.getJumlah()).toLowerCase();
            String deskripsi = t.getDeskripsi().toLowerCase();

            // jika salah satu kolom mengandung keyword â†’ tampilkan
            if (tanggal.contains(keyword)
                    || tipe.contains(keyword)
                    || kategori.contains(keyword)
                    || jumlah.contains(keyword)
                    || deskripsi.contains(keyword)) {
                model.addRow(new Object[]{
                    tanggal,
                    t.getTipe(),
                    t.getKategori(),
                    rupiahFormat.format(t.getJumlah()),
                    t.getDeskripsi()
                });
            }
        }
    }

    // Menghitung ulang total pemasukan, pengeluaran, dan saldo.
    //Hasilnya ditampilkan pada bagian dashboard.
    private void updateDashboard() {
        double totalPemasukan = manager.hitungTotalPemasukan();
        double totalPengeluaran = manager.hitungTotalPengeluaran();
        double saldo = totalPemasukan - totalPengeluaran;

        DecimalFormatSymbols simbol = new DecimalFormatSymbols();
        simbol.setGroupingSeparator('.');
        simbol.setDecimalSeparator(',');
        DecimalFormat formatRupiah = new DecimalFormat("#,###.##", simbol);

        lblPemasukan.setText("Rp " + formatRupiah.format(totalPemasukan));
        lblPengeluaran.setText("Rp " + formatRupiah.format(totalPengeluaran));
        lblSaldo.setText("Rp " + formatRupiah.format(saldo));
    }

    // membersihkan input form
